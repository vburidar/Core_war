#include "../includes/libft.h"

void	*ft_memalloc(size_t size)
{
	char	*mem;
	int		i;

	mem = (char *)malloc(size);
	if (!mem)
		return (0);
	i = 0;
	while (i < (int)size)
		mem[i++] = 0;
	return (mem);
}
